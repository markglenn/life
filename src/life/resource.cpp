#include <life/resource.h>

namespace life
{
    //////////////////////////////////////////////////////////////////////////
    resource::resource( std::string path, std::shared_ptr<life::archive> archive )
        : _path( std::move( path ) ), _archive( std::move( archive ) )
    //////////////////////////////////////////////////////////////////////////
    {
    }

    //////////////////////////////////////////////////////////////////////////
    resource::~resource( )
    //////////////////////////////////////////////////////////////////////////
    {
    }

    //////////////////////////////////////////////////////////////////////////
    const life::archive* resource::archive( ) const
    //////////////////////////////////////////////////////////////////////////
    {
        return _archive.get( );
    }

    //////////////////////////////////////////////////////////////////////////
    const std::string resource::path( ) const
    //////////////////////////////////////////////////////////////////////////
    {
        return _path;
    }
}
